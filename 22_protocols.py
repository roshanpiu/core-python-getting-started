# Protocols - A set of operations that a type must support to implement the protocol
# Do not need to be defined as interfaces or base classes
# Types only need to provide functioning implementations
# Support for a protocol demands a specific behavior from a type 

# Container -> in and not in should be supported
# Sized -> should be able to call len()
# Iterable -> Should be able to Yield items one by one as they requested
# Sequence -> To be able to retrieve items with an integer index ex - sequence[index] ect.